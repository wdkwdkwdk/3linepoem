三行诗简单。易于阅读，易于创作，也易于传播。可能很少有人再去写几百行的长诗了，也少有人研究那些平平仄仄 仄仄平了。<br>
但三行诗不失为一个优秀的种类，因为创作它的自由和简单，但是它又可以在简单中创造无限的想象和韵味。<br>
所以要写好又很考功夫，这意味着，不同水平的人都可以在三行诗的创作中找到乐趣所在。<br>
当人们在一种诗歌形式中找到乐趣时，他们就会关注整个诗歌甚至整个文化，这就是我的目的。
